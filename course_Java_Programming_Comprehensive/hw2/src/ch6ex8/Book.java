package ch6ex8;

public class Book {
    private String name;
    private float cost;

    // constructs new Book with specified arguments
    public Book(String n, float c) {
        name = n;
        cost = c;
    }

    // method to return name of book
    public String getName() {
        return name;
    }

    // method to return cost of book
    public float getCost() {
        return cost;
    }
}

